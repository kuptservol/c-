void Empty() {
    Assert(IsPalindrom(""), "empty");
}

void One() {
    Assert(IsPalindrom("a"), "one");
}

void Correct() {
    AssertFalse(IsPalindrom("a "), "one");
    Assert(IsPalindrom("_a_"), "one");
    AssertFalse(IsPalindrom("bA"), "one");
    AssertFalse(IsPalindrom(" b"), "one");
    Assert(IsPalindrom("bAAb"), "one");
    Assert(IsPalindrom("bACAb"), "one");
}

int main() {
    TestRunner runner;
    runner.RunTest(Empty, "Empty");
    runner.RunTest(One, "One");
    runner.RunTest(Correct, "Correct");
    return 0;
}